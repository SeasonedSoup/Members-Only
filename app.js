const path = require("node:path");
const express = require("express");
const session = require("express-session");
const pgSession = require("connect-pg-simple")(session);
const passport = require("passport");
const LocalStrategy = require("passport-local").Strategy;
const bcrypt = require("bcryptjs");
const pool = require("./db/pool")
const router = require("./routes/userRoute")
const app = express();

app.use(express.urlencoded({extended: true}));
app.set("views", path.join(__dirname, "views"));
app.set("view engine", "ejs");

passport.use(
    new LocalStrategy(async (username, password, done) => {
    try {
        const {rows} = await pool.query(`SELECT * FROM usernames WHERE username = ($1)`, [username]);
        const user = rows[0]

        if (!user) {
            return done(null, false, {message: "Incorrect username"});
        }

        const match = await bcrypt.compare(password, user.password) 
        if (!match) {
            return done(null, false, {message: 'Incorrect password'});
        };

        return done(null, user);

    } catch(err) {
        return done(err);
    }
}))

passport.serializeUser((user, done) => {
  done(null, user.id);
});

passport.deserializeUser(async (id, done) => {
  try {
    const { rows } = await pool.query("SELECT * FROM usernames WHERE id = $1", [id]);
    const user = rows[0];

    done(null, user);
  } catch(err) {
    done(err);
  }
});

app.use(session({
    store: new pgSession({
        pool: pool,
        tableName: 'session'
    }),
    secret: 'apples',
    resave: false,
    saveUninitialized: false,
    cookie: {maxAge: 7 * 24 * 60 * 60 * 1000} //7 day cookie
}));

app.use(passport.initialize());
app.use(passport.session());

app.use((req, res, next) => {
  res.locals.currentUser = req.user;
  next();
});

const PORT = 3000;

app.get("/", (req, res) => {
    res.render("index", {user: req.user});
})
app.post('/log-in', passport.authenticate("local", {
    successRedirect: "/",
    failureRedirect: "/"
}))

app.get("/log-out", (req, res, next) => {
    req.logout((err) => {
        if (err) {
            return next(err);
        }
        res.redirect("/");
    });
});

app.use('/', router);

app.listen((PORT), (error) => {
    if (error) {
        throw error;
    }

    console.log("App is listening at port 3000 visit here http://localhost:3000/")
})



